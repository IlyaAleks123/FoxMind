/** ### OPERATORS ### */
const num1 = 12;
const num2 = 56;

// Arithmetic operators
console.log("+ =", num1 + num2);
console.log("- =", num1 - num2);
console.log("* =", num1 * num2);
console.log("/ =", num1 / num2);

// Comparison operators
console.log("< =", num1 < num2);
console.log("> =", num1 > num2);
console.log("=== =", num1 === num2);