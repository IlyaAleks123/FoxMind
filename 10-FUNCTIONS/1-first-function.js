/* Syntax of function definition
 - `function` - this keyword is used to define a function.
 - function name - this is the name you give to the function. It should be a valid identifier.
 - `(` and `)` - these enclose the list of parameters. If there are no parameters, the parentheses are empty.
 - `{` and `}` - these enclose the block of code that makes up the function.
 */

 function myFirstFunction (){
    console.log("This is my first function");
 }

 //** Call function */
 myFirstFunction();
 myFirstFunction();
