/** ### SWITCH CASE ### */

/** Simple example */
const arg = 2;
switch(arg){
    case 1:
        console.log('I got arg = 1');
        //break;
    case 2:
        console.log('I got arg = 2');
        //break;
    case 3:
        console.log('I got arg = 3');
        //break;
    default:
        console.log('I got unknown argument');
}